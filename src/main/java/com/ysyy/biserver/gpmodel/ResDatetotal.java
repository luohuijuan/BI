package com.ysyy.biserver.gpmodel;

public class ResDatetotal {
    private long year;
    private long month;
    private double ts;
    private double ml;
    private double gvm;
    private long ordercount;
    private double kdj;
    private String plat_id;
    private String plat_name;


    public long getYear() {
        return year;
    }

    public void setYear(long year) {
        this.year = year;
    }


    public long getMonth() {
        return month;
    }

    public void setMonth(long month) {
        this.month = month;
    }


    public double getTs() {
        return ts;
    }

    public void setTs(double ts) {
        this.ts = ts;
    }


    public double getMl() {
        return ml;
    }

    public void setMl(double ml) {
        this.ml = ml;
    }


    public double getGvm() {
        return gvm;
    }

    public void setGvm(double gvm) {
        this.gvm = gvm;
    }


    public long getOrdercount() {
        return ordercount;
    }

    public void setOrdercount(long ordercount) {
        this.ordercount = ordercount;
    }


    public double getKdj() {
        return kdj;
    }

    public void setKdj(double kdj) {
        this.kdj = kdj;
    }
    public String getPlat_id() {
        return plat_id;
    }

    public void setPlat_id(String plat_id) {
        this.plat_id = plat_id;
    }


    public String getPlat_name() {
        return plat_name;
    }

    public void setPlat_name(String plat_name) {
        this.plat_name = plat_name;
    }
}
