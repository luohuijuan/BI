package com.ysyy.biserver.gpmodel;



public class Resymdweek {

  private long year;
  private long quarter;
  private long month;
  private long userid;
  private long mdid;
  private double sfje;
  private java.sql.Date sdate;


  public long getYear() {
    return year;
  }

  public void setYear(long year) {
    this.year = year;
  }


  public long getQuarter() {
    return quarter;
  }

  public void setQuarter(long quarter) {
    this.quarter = quarter;
  }


  public long getMonth() {
    return month;
  }

  public void setMonth(long month) {
    this.month = month;
  }


  public long getUserid() {
    return userid;
  }

  public void setUserid(long userid) {
    this.userid = userid;
  }


  public long getMdid() {
    return mdid;
  }

  public void setMdid(long mdid) {
    this.mdid = mdid;
  }


  public double getSfje() {
    return sfje;
  }

  public void setSfje(double sfje) {
    this.sfje = sfje;
  }


  public java.sql.Date getSdate() {
    return sdate;
  }

  public void setSdate(java.sql.Date sdate) {
    this.sdate = sdate;
  }

}
